import { useState, useEffect } from 'react';
import { motion } from 'framer-motion';
import { 
  Clock, 
  CheckCircle, 
  Truck, 
  XCircle,
  Search,
  Filter,
  Eye,
  Phone,
  MapPin,
  Bell,
  BellRing
} from 'lucide-react';
import { OrderService } from '../../services/orderService';
import OrderDetailsModal from './OrderDetailsModal';
import { getDisplayOrderNumber } from '../../utils/orderUtils';

const AdminOrders = () => {
  const [orders, setOrders] = useState([]);
  const [loading, setLoading] = useState(true);
  const [newOrdersCount, setNewOrdersCount] = useState(0);
  const [lastOrderCount, setLastOrderCount] = useState(0);

  const [searchQuery, setSearchQuery] = useState('');
  const [statusFilter, setStatusFilter] = useState('all');
  const [selectedOrder, setSelectedOrder] = useState(null);

  // –ó–∞–≥—Ä—É–∂–∞–µ–º –∑–∞–∫–∞–∑—ã –∏–∑ Supabase
  useEffect(() => {
    const loadOrders = async () => {
      try {
        setLoading(true);
        console.log('üîç –ó–∞–≥—Ä—É–∂–∞–µ–º –∑–∞–∫–∞–∑—ã –¥–ª—è —Å–ø–∏—Å–∫–∞...');
        const ordersData = await OrderService.getAllOrders();
        console.log('üìä –ó–∞–∫–∞–∑—ã –∑–∞–≥—Ä—É–∂–µ–Ω—ã –≤ —Å–ø–∏—Å–∫–µ:', ordersData);
        setOrders(ordersData);
        setLastOrderCount(ordersData.length);
      } catch (error) {
        console.error('–û—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ –∑–∞–∫–∞–∑–æ–≤:', error);
      } finally {
        setLoading(false);
      }
    };

    loadOrders();
  }, []);

  // –û—Ç—Å–ª–µ–∂–∏–≤–∞–µ–º –Ω–æ–≤—ã–µ –∑–∞–∫–∞–∑—ã
  useEffect(() => {
    const currentOrderCount = orders.length;
    if (currentOrderCount > lastOrderCount) {
      setNewOrdersCount(currentOrderCount - lastOrderCount);
    }
    setLastOrderCount(currentOrderCount);
  }, [orders, lastOrderCount]);

  // –°–±—Ä–∞—Å—ã–≤–∞–µ–º —Å—á–µ—Ç—á–∏–∫ –Ω–æ–≤—ã—Ö –∑–∞–∫–∞–∑–æ–≤ –ø—Ä–∏ –∏–∑–º–µ–Ω–µ–Ω–∏–∏ —Ñ–∏–ª—å—Ç—Ä–æ–≤
  useEffect(() => {
    if (statusFilter === 'all' || statusFilter === 'pending') {
      setNewOrdersCount(0);
    }
  }, [statusFilter]);

  const statusOptions = [
    { value: 'all', label: '–í—Å–µ –∑–∞–∫–∞–∑—ã', color: 'gray' },
    { value: 'pending', label: '–û–∂–∏–¥–∞–µ—Ç', color: 'yellow' },
    { value: 'preparing', label: '–ì–æ—Ç–æ–≤–∏—Ç—Å—è', color: 'blue' },
    { value: 'delivering', label: '–î–æ—Å—Ç–∞–≤–ª—è–µ—Ç—Å—è', color: 'purple' },
    { value: 'completed', label: '–ó–∞–≤–µ—Ä—à–µ–Ω', color: 'green' },
    { value: 'cancelled', label: '–û—Ç–º–µ–Ω–µ–Ω', color: 'red' }
  ];

  const filteredOrders = orders.filter(order => {
    const matchesSearch = order.customer_name?.toLowerCase().includes(searchQuery.toLowerCase()) ||
                         order.phone?.includes(searchQuery) ||
                         order.id.toString().includes(searchQuery);
    const matchesStatus = statusFilter === 'all' || order.status === statusFilter;
    return matchesSearch && matchesStatus;
  });

  const handleStatusChange = async (orderId, newStatus) => {
    try {
      // –û–±–Ω–æ–≤–ª—è–µ–º —Å—Ç–∞—Ç—É—Å –≤ Supabase
      const updatedOrder = await OrderService.updateOrderStatus(orderId, newStatus);
      
      if (updatedOrder) {
        // –û–±–Ω–æ–≤–ª—è–µ–º –ª–æ–∫–∞–ª—å–Ω–æ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ
        setOrders(prevOrders => 
          prevOrders.map(order => 
            order.id === orderId ? updatedOrder : order
          )
        );
      }
    } catch (error) {
      console.error('–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏ —Å—Ç–∞—Ç—É—Å–∞ –∑–∞–∫–∞–∑–∞:', error);
      alert('–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–∏ —Å—Ç–∞—Ç—É—Å–∞ –∑–∞–∫–∞–∑–∞');
    }
  };

  const getStatusColor = (status) => {
    switch (status) {
      case 'pending': return 'bg-yellow-100 text-yellow-800';
      case 'preparing': return 'bg-blue-100 text-blue-800';
      case 'delivering': return 'bg-purple-100 text-purple-800';
      case 'completed': return 'bg-green-100 text-green-800';
      case 'cancelled': return 'bg-red-100 text-red-800';
      default: return 'bg-gray-100 text-gray-800';
    }
  };

  const getStatusText = (status) => {
    switch (status) {
      case 'pending': return '–û–∂–∏–¥–∞–µ—Ç';
      case 'preparing': return '–ì–æ—Ç–æ–≤–∏—Ç—Å—è';
      case 'delivering': return '–î–æ—Å—Ç–∞–≤–ª—è–µ—Ç—Å—è';
      case 'completed': return '–ó–∞–≤–µ—Ä—à–µ–Ω';
      case 'cancelled': return '–û—Ç–º–µ–Ω–µ–Ω';
      default: return '–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–æ';
    }
  };

  const formatTime = (timeString) => {
    return new Date(timeString).toLocaleString('ru-RU', {
      day: '2-digit',
      month: '2-digit',
      hour: '2-digit',
      minute: '2-digit'
    });
  };

  if (loading) {
    return (
      <div className="flex items-center justify-center h-64">
        <div className="text-lg text-gray-600">–ó–∞–≥—Ä—É–∑–∫–∞ –∑–∞–∫–∞–∑–æ–≤...</div>
      </div>
    );
  }

  return (
    <div className="space-y-6">
      {/* Header */}
      <div>
        <div className="flex items-center justify-between mb-2">
          <h2 className="text-2xl font-bold text-gray-900">–£–ø—Ä–∞–≤–ª–µ–Ω–∏–µ –∑–∞–∫–∞–∑–∞–º–∏</h2>
          {newOrdersCount > 0 && (
            <motion.div
              initial={{ scale: 0 }}
              animate={{ scale: 1 }}
              className="flex items-center bg-red-100 text-red-800 px-3 py-1 rounded-full text-sm font-medium"
            >
              <BellRing className="w-4 h-4 mr-1" />
              {newOrdersCount} –Ω–æ–≤—ã–π –∑–∞–∫–∞–∑
            </motion.div>
          )}
        </div>
        <p className="text-gray-600">–ü—Ä–æ—Å–º–∞—Ç—Ä–∏–≤–∞–π—Ç–µ –∏ –æ–±–Ω–æ–≤–ª—è–π—Ç–µ —Å—Ç–∞—Ç—É—Å—ã –∑–∞–∫–∞–∑–æ–≤</p>
      </div>

      {/* Filters */}
      <div className="bg-white rounded-lg shadow-lg p-6">
        <div className="flex flex-col md:flex-row gap-4">
          <div className="flex-1">
            <div className="relative">
              <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 text-gray-400 w-5 h-5" />
              <input
                type="text"
                placeholder="–ü–æ–∏—Å–∫ –ø–æ –∫–ª–∏–µ–Ω—Ç—É, —Ç–µ–ª–µ—Ñ–æ–Ω—É –∏–ª–∏ ID –∑–∞–∫–∞–∑–∞..."
                value={searchQuery}
                onChange={(e) => setSearchQuery(e.target.value)}
                className="w-full pl-10 pr-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-primary-500 focus:border-transparent"
              />
            </div>
          </div>
          <div className="md:w-48">
            <select
              value={statusFilter}
              onChange={(e) => setStatusFilter(e.target.value)}
              className="w-full px-3 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-primary-500 focus:border-transparent"
            >
              {statusOptions.map(option => (
                <option key={option.value} value={option.value}>
                  {option.label}
                </option>
              ))}
            </select>
          </div>
        </div>
      </div>

      {/* Orders List */}
      <div className="space-y-4">
        {filteredOrders.map((order, index) => {
          // –û–ø—Ä–µ–¥–µ–ª—è–µ–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ –∑–∞–∫–∞–∑ –Ω–æ–≤—ã–º (–ø–µ—Ä–≤—ã–µ 3 –∑–∞–∫–∞–∑–∞ —Å—á–∏—Ç–∞—é—Ç—Å—è –Ω–æ–≤—ã–º–∏)
          const isNewOrder = index < 3 && order.status === 'pending';
          
          return (
            <motion.div
              key={order.id}
              layout
              initial={{ opacity: 0, y: 20 }}
              animate={{ opacity: 1, y: 0 }}
              className={`bg-white rounded-lg shadow-lg p-6 ${
                isNewOrder ? 'ring-2 ring-red-200 bg-red-50' : ''
              }`}
            >
            <div className="flex items-start justify-between mb-4">
              <div className="flex-1">
                <div className="flex items-center space-x-4 mb-2">
                  <h3 className="text-lg font-semibold text-gray-900">
                    –ó–∞–∫–∞–∑ {getDisplayOrderNumber(order)}
                  </h3>
                  <span className={`inline-flex px-2 py-1 text-xs font-medium rounded-full ${getStatusColor(order.status)}`}>
                    {getStatusText(order.status)}
                  </span>
                  {isNewOrder && (
                    <motion.span
                      initial={{ scale: 0 }}
                      animate={{ scale: 1 }}
                      className="inline-flex px-2 py-1 text-xs font-medium rounded-full bg-red-100 text-red-800"
                    >
                      –ù–û–í–´–ô
                    </motion.span>
                  )}
                </div>
                
                <div className="grid grid-cols-1 md:grid-cols-3 gap-4 text-sm text-gray-600">
                  <div className="flex items-center">
                    <Phone className="w-4 h-4 mr-2" />
                    <span>{order.customer_name} - {order.phone}</span>
                  </div>
                  <div className="flex items-center">
                    <MapPin className="w-4 h-4 mr-2" />
                    <span>{order.delivery_type === 'delivery' ? order.address : '–°–∞–º–æ–≤—ã–≤–æ–∑'}</span>
                  </div>
                  <div className="flex items-center">
                    <Clock className="w-4 h-4 mr-2" />
                    <span>{formatTime(order.created_at)}</span>
                  </div>
                </div>
              </div>
              
              <div className="text-right">
                <div className="text-2xl font-bold text-primary-600">{order.final_total} ‚ÇΩ</div>
                <div className="text-sm text-gray-500">{order.order_items?.length || 0} –ø–æ–∑–∏—Ü–∏–π</div>
              </div>
            </div>

            {/* Order Items */}
            <div className="mb-4">
              <h4 className="font-medium text-gray-900 mb-2">–ü–æ–∑–∏—Ü–∏–∏ –∑–∞–∫–∞–∑–∞:</h4>
              <div className="space-y-2">
                {order.order_items?.map((item, index) => (
                  <div key={index} className="flex justify-between text-sm">
                    <span>{item.dish_name} x{item.quantity}</span>
                    <span>{item.price * item.quantity} ‚ÇΩ</span>
                  </div>
                )) || <div className="text-sm text-gray-500">–ü–æ–∑–∏—Ü–∏–∏ –Ω–µ –Ω–∞–π–¥–µ–Ω—ã</div>}
              </div>
            </div>

            {/* Order Details */}
            <div className="grid grid-cols-1 md:grid-cols-2 gap-4 mb-4 text-sm">
              <div>
                <span className="text-gray-500">–°–ø–æ—Å–æ–± –ø–æ–ª—É—á–µ–Ω–∏—è:</span>
                <span className="ml-2 font-medium">
                  {order.delivery_type === 'delivery' ? '–î–æ—Å—Ç–∞–≤–∫–∞' : '–°–∞–º–æ–≤—ã–≤–æ–∑'}
                </span>
              </div>
              <div>
                <span className="text-gray-500">–°–ø–æ—Å–æ–± –æ–ø–ª–∞—Ç—ã:</span>
                <span className="ml-2 font-medium">
                  {order.payment_method === 'cash' ? '–ù–∞–ª–∏—á–Ω—ã–µ' : '–ö–∞—Ä—Ç–∞'}
                </span>
              </div>
              {order.notes && (
                <div className="md:col-span-2">
                  <span className="text-gray-500">–ö–æ–º–º–µ–Ω—Ç–∞—Ä–∏–π:</span>
                  <span className="ml-2 font-medium">{order.notes}</span>
                </div>
              )}
            </div>

            {/* Action Buttons */}
            <div className="flex flex-wrap gap-2">
              {order.status === 'pending' && (
                <>
                  <button
                    onClick={() => handleStatusChange(order.id, 'preparing')}
                    className="bg-blue-100 text-blue-700 px-3 py-2 rounded-lg hover:bg-blue-200 transition-colors flex items-center"
                  >
                    <Clock className="w-4 h-4 mr-1" />
                    –ù–∞—á–∞—Ç—å –≥–æ—Ç–æ–≤–∏—Ç—å
                  </button>
                  <button
                    onClick={() => handleStatusChange(order.id, 'cancelled')}
                    className="bg-red-100 text-red-700 px-3 py-2 rounded-lg hover:bg-red-200 transition-colors flex items-center"
                  >
                    <XCircle className="w-4 h-4 mr-1" />
                    –û—Ç–º–µ–Ω–∏—Ç—å
                  </button>
                </>
              )}
              
              {order.status === 'preparing' && (
                <>
                  <button
                    onClick={() => handleStatusChange(order.id, 'delivering')}
                    className="bg-purple-100 text-purple-700 px-3 py-2 rounded-lg hover:bg-purple-200 transition-colors flex items-center"
                  >
                    <Truck className="w-4 h-4 mr-1" />
                    –û—Ç–ø—Ä–∞–≤–∏—Ç—å –Ω–∞ –¥–æ—Å—Ç–∞–≤–∫—É
                  </button>
                  {order.delivery_type === 'pickup' && (
                    <button
                      onClick={() => handleStatusChange(order.id, 'completed')}
                      className="bg-green-100 text-green-700 px-3 py-2 rounded-lg hover:bg-green-200 transition-colors flex items-center"
                    >
                      <CheckCircle className="w-4 h-4 mr-1" />
                      –ì–æ—Ç–æ–≤ –∫ –≤—ã–¥–∞—á–µ
                    </button>
                  )}
                </>
              )}
              
              {order.status === 'delivering' && (
                <button
                  onClick={() => handleStatusChange(order.id, 'completed')}
                  className="bg-green-100 text-green-700 px-3 py-2 rounded-lg hover:bg-green-200 transition-colors flex items-center"
                >
                  <CheckCircle className="w-4 h-4 mr-1" />
                  –ó–∞–≤–µ—Ä—à–∏—Ç—å –¥–æ—Å—Ç–∞–≤–∫—É
                </button>
              )}
              
              <button
                onClick={() => setSelectedOrder(order)}
                className="bg-gray-100 text-gray-700 px-3 py-2 rounded-lg hover:bg-gray-200 transition-colors flex items-center"
              >
                <Eye className="w-4 h-4 mr-1" />
                –ü–æ–¥—Ä–æ–±–Ω–æ—Å—Ç–∏
              </button>
            </div>
          </motion.div>
          );
        })}
      </div>

      {/* Order Details Modal */}
      <OrderDetailsModal
        order={selectedOrder}
        isOpen={!!selectedOrder}
        onClose={() => setSelectedOrder(null)}
      />
    </div>
  );
};

export default AdminOrders;
